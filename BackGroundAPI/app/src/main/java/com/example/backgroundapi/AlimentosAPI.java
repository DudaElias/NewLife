package com.example.backgroundapi;

public class AlimentosAPI
{
    int id;
    String nome;
    int carboidrato;

    public AlimentosAPI()
    {

    }

    int fibras;
    int sodio;
    int calorias;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getCarboidrato() {
        return carboidrato;
    }

    public void setCarboidrato(int carboidrato) {
        this.carboidrato = carboidrato;
    }

    public int getFibras() {
        return fibras;
    }

    public void setFibras(int fibras) {
        this.fibras = fibras;
    }

    public int getSodio() {
        return sodio;
    }

    public void setSodio(int sodio) {
        this.sodio = sodio;
    }

    public int getCalorias() {
        return calorias;
    }

    public void setCalorias(int calorias) {
        this.calorias = calorias;
    }

}
